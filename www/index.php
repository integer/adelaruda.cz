    <div class="gestalt-terapie" id="gestalt-terapie">
        <h3>Gestalt terapie</h3>
        <p>Ve své práci vycházím z principů <a href="https://www.gestalt-praha.cz/co-je-gestalt-terapie/" target="_blank">Gestalt terapie</a>.</p> 
        <p>Hlavním principem gestalt přístupu je zaměření na "tady a teď". Klade důraz na naše aktuální uvědomění a zkušenost v přítomném okamžiku. Tedy i s minulostí a budoucností pracuje z pozice aktuální situace, tak jak ji prožíváme a zakoušíme právě teď a tady.</p>
        <p></p>Základem gestalt terapie je terapeutický vztah, který je rozvíjen vzájemným dialogem, klade důraz na kontakt a autenticitu.</p>
        <p>Gestalt přístup vychází z fenomenologie. Tedy předpokládá, že prožívání každého z nás je jedinečné a subjektivní. Terapeut toto uznává a nabízí zpětnou vazbu, nikoliv interpretaci. </p>
        <p>Cílem gestalt terapie je prohlubovat naše uvědomění. Předpokládá, že zacházet můžeme jen s tím, co si dokážeme uvědomit. K prohloubení uvědomění a růstu využívá experimenty a kreativní techniky.</p>
    </div>
    <div class="ja-a-gestalt" id="ja-a-gestalt">
        <h5>Já a Gestalt</h5>
        <p>Principy Gestalt terapie ladí s mými hodnotami a životním stylem. Jako terapeutka tak mohu být ve své práci autentická v tom, co dělám a říkám.</p>
        <p>Usiluji o to druhé nehodnotit, ale pochopit. Nejen jejich jednání ale především prožívání. Schopnost vžít se do toho, "že chodím v botách někoho jiného", mě přivádí k respektu a porozumění.</p> 
        <p>Během jednotlivých setkání ani celého procesu se neopírám o předem danou strukturu. S důvěrou a kreativitou pracuji s klienty na tom, co aktuálně přináší. </p>
